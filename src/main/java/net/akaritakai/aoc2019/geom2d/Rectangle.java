package net.akaritakai.aoc2019.geom2d;

public class Rectangle {
  public final long x;
  public final long y;
  public final long width;
  public final long height;

  public Rectangle(long x, long y, long width, long height) {
    this.x = x;
    this.y = y;
    this.width = width;
    this.height = height;
  }
}
